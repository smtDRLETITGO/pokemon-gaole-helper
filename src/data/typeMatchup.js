const ALL_TYPES = ['一般','火','水','電','草','冰','格鬥','毒','地面','飛行','超能力','蟲','岩石','幽靈','龍','惡','鋼','妖精'];

/**
 * 計算對抗指定防守方屬性（可為雙屬性）的弱點（攻擊方倍率 >= 2 的屬性）
 * @param {string[]} defendingTypes - 對手的屬性陣列 (例如: ['草', '毒'])
 * @returns {string[]} 推薦的攻擊屬性陣列 (例如: ['火', '飛行', '冰', '超能力'])
 */
export function getWeaknesses(defendingTypes) {
  if (!defendingTypes || defendingTypes.length === 0) return [];
  
  const weaknesses = [];

  for (const attacker of ALL_TYPES) {
    const multiplier = getAttackMultiplier(attacker, defendingTypes);
    // 倍率 >= 2 視為效果絕佳
    if (multiplier >= 2) {
      weaknesses.push(attacker);
    }
  }

  return weaknesses;
}

/**
 * 計算攻擊方對防守方屬性的總傷害倍率
 * @param {string} attackType - 攻擊方屬性
 * @param {string[]} defendingTypes - 防守方屬性陣列
 * @returns {number} 傷害倍率 (例如: 4, 2, 1, 0.5, 0)
 */
export function getAttackMultiplier(attackType, defendingTypes) {
  if (!defendingTypes || defendingTypes.length === 0) return 1;
  let multiplier = 1;
  for (const defender of defendingTypes) {
    const match = TYPE_MATCHUP[attackType]?.[defender];
    if (match !== undefined) {
      multiplier *= match;
    }
  }
  return multiplier;
}
